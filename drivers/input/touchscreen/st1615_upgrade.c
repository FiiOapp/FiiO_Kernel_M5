{
	
	int ret = 0;
	if(txbuf == NULL)
		return -1;

	ret = i2c_master_send(client, txbuf, len);
	if (ret < 0){
		printk("write 0x%x error (%d)\n", *txbuf, ret);
		return ret;
	}
	return len;
}
