static int st_i2c_read_direct(struct i2c_client *client,u8 *rxbuf, int len)
{
	
	int ret = 0;	
	ret = i2c_master_recv(client, rxbuf, len);

	if (ret < 0){
		printk("read direct error (%d)\n", ret);
		return ret;
	}
	return len;
}

static int st_i2c_read_bytes(struct i2c_client *client,u8 addr, u8 *rxbuf, int len)
{
	
	int ret = 0;
	u8 txbuf = addr;
	
	ret = i2c_master_send(client, &txbuf, 1);
	if (ret < 0){
		printk("write 0x%x error (%d)\n", addr, ret);
		return ret;
	}
	ret = i2c_master_recv(client, rxbuf, len);

	if (ret < 0){
		printk("read 0x%x error (%d)\n", addr, ret);
		return ret;
	}
	return len;
}

